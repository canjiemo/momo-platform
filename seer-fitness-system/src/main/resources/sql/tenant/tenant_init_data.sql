-- 新架构说明 (2025-10-17 更新):
--
-- 1. 菜单初始化
--    ❌ 旧方式: 通过 SQL 脚本在租户 schema 中插入菜单数据
--    ✅ 新方式: 平台统一管理菜单模板 (public.sys_menu)
--             - 租户创建时不插入任何菜单数据
--             - 通过平台管理接口分配菜单给租户 (sys_tenant_menu)
--             - 分配时自动将菜单数据复制到租户 schema (tenant_xxx.sys_menu)
--             - 菜单通过 platform_menu_id 字段关联平台模板
--
-- 2. 角色初始化
--    ❌ 旧方式: 通过 SQL 脚本预置角色和权限
--    ✅ 新方式: 租户管理员自行创建角色
--             - 租户创建时不插入任何角色数据
--             - 租户管理员登录后根据实际需求创建角色
--             - 通过角色管理模块分配权限 (关联已分配的菜单)
--
-- 3. 超级管理员初始化
--    ❌ 旧方式: 通过 SQL 脚本插入固定的管理员账号
--    ✅ 新方式: Java 代码动态创建
--             - TenantSchemaService.createAdminUser() 方法创建
--             - 支持自定义用户名、真实姓名、密码
--             - 设置 admin_flag=1 (超级管理员标记)
--             - 自动使用雪花算法生成 ID
--
-- 4. 用户-角色关联
--    ❌ 旧方式: SQL 脚本预置关联关系
--    ✅ 新方式: 租户管理员动态分配
--             - 通过用户管理接口分配角色
--             - 灵活调整权限配置
--
-- ====================================================================================================
-- 数据流程图
-- ====================================================================================================
--
-- 租户创建流程:
--
--   1. 创建租户记录 (public.sys_tenant)
--         ↓
--   2. 创建租户 Schema (CREATE SCHEMA tenant_xxx)
--         ↓
--   3. 执行 DDL 脚本创建表结构 (tenant_schema_template.sql)
--         ↓
--   4. Java 代码创建超级管理员 (TenantSchemaService.createAdminUser)
--         ↓
--   5. [完成] 租户可以登录使用
--
-- 菜单分配流程:
--
--   1. 平台管理员在 public.sys_menu 中管理菜单模板
--         ↓
--   2. 通过菜单分配接口选择菜单分配给租户 (sys_tenant_menu)
--         ↓
--   3. 自动将菜单数据复制到租户 schema (tenant_xxx.sys_menu)
--         ↓
--   4. 租户用户可以看到分配的菜单并创建角色关联权限
--
-- ====================================================================================================
-- 相关文件
-- ====================================================================================================
--
-- DDL 模板:
--   - tenant_schema_template.sql (表结构定义)
--
-- Java 服务:
--   - TenantSchemaService.java (Schema 创建和初始化)
--   - PlatformMenuService.java (平台菜单管理)
--   - TenantMenuAssignmentService.java (菜单分配)
--   - TenantMenuService.java (租户菜单查询，只读)
--
-- ====================================================================================================
-- 设计原则
-- ====================================================================================================
--
-- 1. SQL 模板仅用于结构，不用于数据
--    - SQL 脚本只负责创建 Schema 和表结构
--    - 所有业务数据通过 Java 代码或 API 管理
--
-- 2. 平台统一管控功能
--    - 菜单 = 系统功能点，由平台统一管理
--    - 租户不能自定义菜单 (避免功能碎片化)
--    - 平台可以灵活控制租户可用功能
--
-- 3. 数据复制实现透明性
--    - 菜单数据复制到租户 schema
--    - 开发者无需关心数据来源 (统一访问本地表)
--    - 通过 platform_menu_id 保持同步能力
--
-- 4. 灵活的权限配置
--    - 租户管理员创建符合自身需求的角色
--    - 基于已分配菜单自由组合权限
--    - 支持精细化的权限控制
--
